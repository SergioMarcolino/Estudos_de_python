# Int
int_number = 3 # int
print("Int =", int_number)

# Real with floating point
real_number = 3.14 # float
print("Real number = ", real_number)

# type() function, this function returns the type of variable

print("Type of int variable = ", type(int_number))
print("Type of real variable = ", type(real_number))


# sum +
sum = 1 + 1
print("1 + 1 = ", sum)

# subtraction -
subtraction = 10 - 5

print("10 - 5 = ", subtraction)

# multiplied *
multiplied = 10 * 2
print("10 * 2 = ", multiplied)

# division /
division = 5 / 2
print("5 / 2 = ", division)

# int() this function converts the value to an integer

print("Conversao do valor divisao para int = ", int(division))

# float(), this function converts the value to an floating 
print("Conversao do valor divisao para float = ", float(division))

#module % reminder of the division
module = 5 % 2
print("5 % 2 = ", module)

# integer divided  /
divided = 5 // 2
print("5 // 2 = ", divided)